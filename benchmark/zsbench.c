/* zsbench - A tool to benchmark Zeroskip. This is based on the db_bench tool
 *           in LevelDB.
 */

#include <assert.h>
#include <errno.h>
#include <getopt.h>
#include <inttypes.h>
#include <libgen.h>
#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <sys/stat.h>
#include <time.h>
#include <unistd.h>

#include <libzeroskip/util.h>
#include <libzeroskip/zeroskip.h>

#include "bench-common.h"

/* Globals */
static char *DBNAME;
static const char *BENCHMARKS;
static int NUMRECS = 1000;
static int new_db = 0;          /* set to 1 if we created a new db */
static size_t VALLEN = 0;

enum {
        BATCHED,
        NOTBATCHED,
        SEQUENTIAL,
        RANDOM,
};

static struct option long_options[] = {
        {"benchmarks", required_argument, NULL, 'b'},
        {"db", required_argument, NULL, 'd'},
        {"numrecs", optional_argument, NULL, 'n'},
        {"help", no_argument, NULL, 'h'},
        {NULL, 0, NULL, 0}
};

static void usage(const char *progname)
{
        printf("Usage: %s [OPTION]... [DB]...\n", progname);

        printf("  -b, --benchmarks     comma separated list of benchmarks to run\n");
        printf("                       Available benchmarks:\n");
        printf("                       * writeseq       - write values in sequential key order\n");
        printf("                       * writeseqtxn    - write values in sequential key order in separate transactions\n");
        printf("                       * writerandom    - write values in random key order\n");
        printf("                       * writerandomtxn - write values in random key order in separate transactions\n");
        printf("                       * overwriterandom- overwrite values in random key order in separate transactions\n");
        printf("                       * write100k      - write values 100K long in random key order\n");
        printf("\n");
        printf("                       * open           - cost of opening a DB\n");
        printf("\n");
        printf("  -d, --db             the db to run the benchmarks on\n");
        printf("  -n, --numrecs        number of records to write[default: 1000]\n");
        printf("  -h, --help           display this help and exit\n");
}

#define ALLBENCHMARKS "writeseq,writeseqtxn,writerandom,writerandomtxn,overwriterandom,write100k,open"

static char *create_tmp_dir_name(void)
{
       static const char charset[] =
                "abcdefghijklmnopqrstuvwxyz"
                "ABCDEFGHIJKLMNOPQRSTUVWXYZ"
                "0123456789";
        static const int num_chars = 62;
        uint64_t value;
        struct timeval tv;
        const char *tmpdir;
        char path[PATH_MAX];
        char *dirname_template;
        int i, ret = 1;
        struct stat sb;

        gettimeofday(&tv, NULL);
        value = ((size_t)(tv.tv_usec << 16)) ^ tv.tv_sec ^ getpid();

        tmpdir = getenv("TMPDIR");
        if (!tmpdir)
                tmpdir = "/tmp";

        snprintf(path, sizeof(path), "%s/zsbench-XXXXXX", tmpdir);

        dirname_template = &path[strlen(path) - 6];

        /* TMP_MAX: The minimum number of unique filenames generated by
         * tmpnam() */
        for (i = 0; i < TMP_MAX; ++i) {
                uint64_t v = value;
                int j;

                ret = 1;

                /* Fill in the random bits. */
                for (j = 0; j < 6; j++) {
                        dirname_template[j] = charset[v % num_chars];
                        v /= num_chars;
                }

                if (stat(path, &sb) == -1) {
                        if (errno == ENOENT) {
                                /* FOUND an unused path! */
                                ret = 0;
                                break;
                        }
                }

                value += 9999;
        }

        if (!ret)
                return xstrdup(path);
        else
                return NULL;
}

static char *generate_random_string(char *str, size_t length)
{
        static const char charset[] =
                "abcdefghijklmnopqrstuvwxyz"
                "ABCDEFGHIJKLMNOPQRSTUVWXYZ"
                "0123456789"
                "!@#$%^&*()-=_+|{}[];<>,./?:";

        if (length) {
                --length;

                for (size_t n = 0; n < length; n++) {
                        int pos = rand() % (int) (sizeof charset - 1);
                        str[n] = charset[pos];
                }

                str[length] = '\0';
        }

        return str;
}

static char *random_string(size_t length)
{
     char *s = xmalloc(length + 1);

     generate_random_string(s, length);

     return s;
}

static void cleanup_db_dir(void)
{
        recursive_rm(DBNAME);
}

static size_t do_write(int txnmode, int insmode)
{
        int i;
        int ret;
        struct zsdb *db = NULL;
        size_t bytes = 0;
        struct zsdb_txn *txn = NULL;

        /* Open Zeroskip DB */
        ret = zsdb_init(&db, NULL, NULL);
        assert(ret == ZS_OK);
        ret = zsdb_open(db, DBNAME, new_db ? MODE_CREATE : MODE_RDWR);
        assert(ret == ZS_OK);

        zsdb_write_lock_acquire(db, 0);

        for (i = 0; i < NUMRECS; i++) {
                char key[100];
                size_t keylen, vallen;
                char *val;
                int k;

                k = (insmode == SEQUENTIAL) ? i : ((time(NULL) *  i) % NUMRECS);

                snprintf(key, sizeof(key), "%016d", k);
                keylen = strlen(key);
                vallen = VALLEN ? VALLEN : keylen * 2;
                val = random_string(vallen);

                if (txnmode == BATCHED) {
                        ret = zsdb_transaction_begin(db, &txn);
                        assert(ret == ZS_OK);
                }

                ret = zsdb_add(db, (unsigned char *)key, keylen,
                               (unsigned char *)val, vallen, &txn);

                assert(ret == ZS_OK);
                bytes += (keylen + vallen);

                if (txnmode == BATCHED) {
                        ret = zsdb_commit(db, &txn);
                        assert(ret == ZS_OK);
                }
        }

        zsdb_write_lock_release(db);

        if (txnmode == NOTBATCHED)
                zsdb_commit(db, NULL);

        /* Close Zeroskip DB */
        ret = zsdb_close(db);
        assert(ret == ZS_OK);
        zsdb_final(&db);


        return bytes;
}

static void do_open(int num_iters)
{
        struct zsdb *db = NULL;
        int ret, j;

        for (j = 0; j < num_iters; j++) {
                ret = zsdb_init(&db, NULL, NULL);
                assert(ret == ZS_OK);
                ret = zsdb_open(db, DBNAME, new_db ? MODE_CREATE : MODE_RDWR);
                assert(ret == ZS_OK);
                ret = zsdb_close(db);
                assert(ret == ZS_OK);
                zsdb_final(&db);
                db = NULL;
        }
}

static int parse_options(int argc, char **argv, const struct option *options)
{
        int option;
        int option_index;

        while ((option = getopt_long(argc, argv, "d:b:h?",
                                     long_options, &option_index)) != -1) {
                switch (option) {
                case 'b':
                        BENCHMARKS = optarg;
                        break;
                case 'd':
                        DBNAME = optarg;
                        break;
                case 'n':
                        NUMRECS = atoi(optarg);
                        break;
                case 'h':
                case '?':
                        usage(basename(argv[0]));
                        exit(option == 'h');
                }
        }

        return 0;
}

static int run_benchmarks(void)
{
        struct str_array benchmarks;
        int i;
        uint64_t start, finish;
        size_t bytes;

        print_header();

        str_array_init(&benchmarks);

        str_array_from_strsplit(&benchmarks, BENCHMARKS, ',');

        for (i = 0; i < benchmarks.count; i++) {
                if (strcmp(benchmarks.datav[i], "writeseq") == 0) {
                        start = get_time_now();
                        bytes = do_write(NOTBATCHED, SEQUENTIAL);
                        finish = get_time_now();

                        fprintf(stderr, "writeseq        : %zu bytes written in %" PRIu64 " μs.\n",
                                bytes, (finish - start));
                } else if (strcmp(benchmarks.datav[i], "writeseqtxn") == 0) {
                        start = get_time_now();
                        bytes = do_write(BATCHED, SEQUENTIAL);
                        finish = get_time_now();

                        fprintf(stderr, "writeseqtxn     : %zu bytes written in %" PRIu64 " μs.\n",
                                bytes, (finish - start));
                } else if (strcmp(benchmarks.datav[i], "writerandom") == 0) {
                        start = get_time_now();
                        bytes = do_write(NOTBATCHED, RANDOM);
                        finish = get_time_now();

                        fprintf(stderr, "writerandom     : %zu bytes written in %" PRIu64 " μs.\n",
                                bytes, (finish - start));
                } else if (strcmp(benchmarks.datav[i], "writerandomtxn") == 0) {
                        start = get_time_now();
                        bytes = do_write(BATCHED, RANDOM);
                        finish = get_time_now();

                        fprintf(stderr, "writerandomtxn  : %zu bytes written in %" PRIu64 " μs.\n",
                                bytes, (finish - start));
                } else if (strcmp(benchmarks.datav[i], "overwriterandom") == 0) {
                        start = get_time_now();
                        bytes = do_write(NOTBATCHED, SEQUENTIAL);
                        new_db = 0;
                        bytes += do_write(NOTBATCHED, RANDOM);
                        new_db = 1;
                        finish = get_time_now();

                        fprintf(stderr, "overwriterandom : %zu bytes written in %" PRIu64 " μs.\n",
                                bytes, (finish - start));
                } else if (strcmp(benchmarks.datav[i], "write100k") == 0) {
                        VALLEN = 100 * 1000;
                        start = get_time_now();
                        bytes = do_write(NOTBATCHED, RANDOM);
                        finish = get_time_now();

                        fprintf(stderr, "write100k       : %zu bytes written in %" PRIu64 " μs.\n",
                                bytes, (finish - start));
                        VALLEN = 0;
                } else if (strcmp(benchmarks.datav[i], "open") == 0) {
                        int NUM = 1000;

                        start = get_time_now();
                        do_open(NUM);
                        finish = get_time_now();

                        fprintf(stderr, "open            : DB opened %d times in %" PRIu64 " μs.(avg: %" PRIu64 " μs).\n",
                                NUM, (finish - start), (finish - start)/NUM);
                        VALLEN = 0;

                } else {
                        fprintf(stderr, "Unknown benchmark '%s'\n",
                                benchmarks.datav[i]);
                }

                if (new_db) {
                        cleanup_db_dir();
                }
        }

        str_array_clear(&benchmarks);
        return 0;
}

int main(int argc, char **argv)
{
        int ret = EXIT_SUCCESS;
        int seed = 1103515245;

        if (argc == 0) {
                usage(basename(argv[0]));
                ret = EXIT_FAILURE;
                goto done;
        }

        /* Random Seed */
        srand(time(NULL) * seed);

        ret = parse_options(argc, argv, long_options);

        if (BENCHMARKS == NULL) {
                fprintf(stderr, "Running all benchmarks\n");
                BENCHMARKS = ALLBENCHMARKS;
        }

        if (DBNAME == NULL) {
                new_db = 1;
                DBNAME = create_tmp_dir_name();
                assert(DBNAME != NULL);
                printf("Creating a new DB: %s\n", DBNAME);
        } else {
                printf("Using existing DB: %s\n", DBNAME);
        }

        ret = run_benchmarks();

        if (new_db) {
                free(DBNAME);
                DBNAME = NULL;
        }
done:
        exit(ret);
}
